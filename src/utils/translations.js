// Translation hook
export function useTranslation(language = 'en') {
  const t = (key) => {
    const keys = key.split('.')
    let value = translations[language]
    
    for (const k of keys) {
      value = value?.[k]
    }
    
    return value || key // Return key if translation not found
  }
  
  return { t }
}